const FIVE_HOURS_IN_SECONDS: u32 = 60 * 60 * 5;

fn main() {
    println!("Five hours in seconds: {FIVE_HOURS_IN_SECONDS}s");
    const FOUR_HOURS_IN_SECONDS: u32 = 60 * 60 * 4;
    println!("Four hours in seconds: {FOUR_HOURS_IN_SECONDS}s");
    const THREE_HOURS_IN_SECONDS: u32 = 60 * 60 * 3;
    println!("Three hours in seconds: {THREE_HOURS_IN_SECONDS}s");
}
